var nomJour = prompt("Entrez un jour :");

switch(nomJour)
{
 case "Lundi":
    console.log("Mardi");
 break;
 case "Mardi":
    console.log("Mercredi");
 break;
 case "Mercredi":
    console.log("Jeudi");
 break;
 case "Jeudi":
    console.log("Vendredi");
 break;
 case "Vendredi":
    console.log("Samedi");
 break;
 case "Samedi":
    console.log("Dimanche");
 break;
 case "Dimanche":
    console.log("Lundi");
 break;        
}